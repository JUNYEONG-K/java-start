package variable.grammar;

public class Var2 {
    public static void main(String[] args) {
        int a = 10; // 변수 선언 (정수),  초기화

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        a = 20; // 변수 값 수정 10 -> 20
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
    }
}
